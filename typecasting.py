# Converting int to float
a = 10
print(float(a))   # 10.0

# Converting float to int
b = 12.7
print(int(b))     # 12

# Converting int to string
c = 50
print(str(c))     # "50"

# Converting string to int
d = "100"
print(int(d))     # 100

# Converting number to boolean
e = 0
print(bool(e))    # False
f = 5
print(bool(f))    # True
