name = "Farhan"                 # Our string

print(name[0:3])   # "Far" → characters from index 0 to 2
print(name[:4])    # "Farh" → from start to index 3
print(name[2:])    # "rhan" → from index 2 till end
print(name[-3:])   # "han" → last 3 characters
print(name[::-1])  # "nahraF" → reverse the string
