# 2. Assignment Operators
x = 10; print("Initial x:", x) 
x += 5; print("After x += 5:", x)   # x = x + 5 → 15
x -= 3; print("After x -= 3:", x)   # x = x - 3 → 12
x *= 2; print("After x *= 2:", x)   # x = x * 2 → 24
x /= 4; print("After x /= 4:", x)   # x = x / 4 → 6.0
x //= 2; print("After x //= 2:", x) # x = x // 2 → 3.0
x %= 3; print("After x %= 3:", x)   # x = x % 3 → 0.0
x **= 2; print("After x **= 2:", x) # x = x ** 2 → 0.0
print()
