# Python program to demonstrate basic data types

# 1. Integer (whole number without decimal)
integer_number = 10   # Storing an integer value
print("Integer:", integer_number)   # Printing the integer value

# 2. Floating Point Number (number with decimal point)
floating_number = 10.5   # Storing a float value
print("Float:", floating_number)    # Printing the float value

# 3. String (sequence of characters enclosed in quotes)
string_text = "Hello, Python!"   # Storing a string value
print("String:", string_text)    # Printing the string value

# 4. Boolean (True or False values)
is_python_easy = True   # Storing a boolean value (True)
print("Boolean:", is_python_easy)   # Printing the boolean value

# 5. None (represents absence of value or null)
nothing_here = None   # Storing a None value
print("None type:", nothing_here)   # Printing None value

# Checking the type of each variable using type() function
print("\nData Types of Each Variable:")
print("Type of integer_number:", type(integer_number))   # int
print("Type of floating_number:", type(floating_number)) # float
print("Type of string_text:", type(string_text))         # str
print("Type of is_python_easy:", type(is_python_easy))   # bool
print("Type of nothing_here:", type(nothing_here))       # NoneType
