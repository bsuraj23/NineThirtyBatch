# Integer: Whole numbers without a decimal point
age = 25  # This is an integer value

# Floating Point Number: Numbers with a decimal point
height = 5.9  # This is a float value

# String: Sequence of characters enclosed in quotes
name = "Alice"  # This is a string value

# Boolean: Represents True or False values
is_student = True  # This is a boolean value

# None: Represents the absence of a value
address = None  # This means 'address' has no value

# Printing all variables to see their values and types
print("Age:", age, "| Type:", type(age))  # Prints value and type of 'age'
print("Height:", height, "| Type:", type(height))  # Prints value and type of 'height'
print("Name:", name, "| Type:", type(name))  # Prints value and type of 'name'
print("Is Student:", is_student, "| Type:", type(is_student))  # Prints value and type of 'is_student'
print("Address:", address, "| Type:", type(address))  # Prints value and type of