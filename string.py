# Using single quotes
name1 = 'Farhan'
print(name1)   # Farhan

# Using double quotes
name2 = "Farhan"
print(name2)   # Farhan

# Using triple quotes (can span multiple lines)
name3 = '''Farhan'''
print(name3)   # Farhan

# Triple double quotes also work
name4 = """Farhan"""
print(name4)   # Farhan
