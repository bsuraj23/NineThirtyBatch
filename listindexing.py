my_list = [10, 20, 30, 40, 50]      # Our list

print(my_list[0])    # 10 → first element (index 0)
print(my_list[1])    # 20 → second element (index 1)
print(my_list[4])    # 50 → fifth element (index 4)

print(my_list[-1])   # 50 → last element (negative index -1)
print(my_list[-2])   # 40 → second last element (negative index -2)
print(my_list[-5])   # 10 → first element using negative index
