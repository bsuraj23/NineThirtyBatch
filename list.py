# List containing all types of data in Python

my_list = [
    10,                 # Integer
    3.14,               # Float
    "Hello",            # String
    True,               # Boolean
    None,               # NoneType
    [1, 2, 3],          # List inside a list (nested list)
    (4, 5),             # Tuple
    {'a': 1, 'b': 2},   # Dictionary
    {6,7,8}           # Set
]

print(my_list)
