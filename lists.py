# Creating a list with different data types
my_list = [10, 3.14, "Farhan", True, None, 'A']   # int, float, string, boolean, None, character

print(my_list)          # [10, 3.14, 'Farhan', True, None, 'A'] → prints the whole list
print(my_list[0])       # 10 → first element (integer)
print(my_list[1])       # 3.14 → second element (float)
print(my_list[2])       # Farhan → third element (string)
print(my_list[3])       # True → fourth element (boolean)
print(my_list[4])       # None → fifth element (NoneType)
print(my_list[5])       # A → sixth element (character is also a string in Python)