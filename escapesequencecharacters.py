print("Hello\nFarhan")     # \n → newline (moves text to next line)
print("Hello\tFarhan")     # \t → tab space (adds extra space like tab key)
print("Hello\"Farhan\"")   # \" → double quote inside string
print('It\'s Python')      # \' → single quote inside string
print("C:\\Users\\Farhan") # \\ → backslash inside string
print("First line\rFarhan")# \r → carriage return (replaces from beginning with "Farhan")
print("Farhan\b!")         # \b → backspace (removes last character before '!')
