# -------------------------------
# 1. Arithmetic Operators
# -------------------------------

# Addition
a = 10
b = 5
print("Addition:", a + b)   # 10 + 5 = 15

# Subtraction
print("Subtraction:", a - b)  # 10 - 5 = 5

# Multiplication
print("Multiplication:", a * b)  # 10 * 5 = 50

# Division
print("Division:", a / b)  # 10 / 5 = 2.0

# Floor Division
print("Floor Division:", a // b)  # 10 // 5 = 2 (integer result)

# Modulus
print("Modulus:", a % b)  # 10 % 5 = 0 (remainder)

# Exponent
print("Exponent:", a ** b)  # 10 ** 5 = 100000
print()
