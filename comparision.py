# 3. Comparison Operators
p = 7; q = 5
print("p == q:", p == q)   # Equal to → False
print("p != q:", p != q)   # Not equal → True
print("p > q:", p > q)     # Greater than → True
print("p < q:", p < q)     # Less than → False
print("p >= q:", p >= q)   # Greater or equal → True
print("p <= q:", p <= q)   # Less or equal → False
print()
